    """Send next post from CHANNEL_POSTS sequentially."""
    global _post_index
    text = CHANNEL_POSTS[_post_index % len(CHANNEL_POSTS)]
    _post_index += 1
    logger.info(f"Автопост #{_post_index}: отправка в канал")
