        f"✨ *Главная фишка:* {rec['highlight']}\n"
        f"📅 *Лучшее время:* {rec['best_time']}\n"
        f"💰 *Бюджет:* {rec['budget']}\n"
        f"🛂 *Виза:* {rec['visa']}\n"
        f"🎯 *Совет эксперта:* {rec['tip']}\n\n"
